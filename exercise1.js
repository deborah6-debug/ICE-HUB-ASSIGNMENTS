// 1. Create a variable with a Boolean value
let myVariable = true;

// 2. Output the value of the variable to the console
console.log("myVariable =", myVariable);

// 3. Check whether the variable is true
if (myVariable) {
  console.log("The variable is true ✅");
}

// 4. Add another if statement with ! to check if it’s not true
if (!myVariable) {
  console.log("The variable is false ❌");
}

// (Alternative way using if/else)
if (myVariable) {
  console.log("Using if/else → The variable is true ✅");
} else {
  console.log("Using if/else → The variable is false ❌");
}
